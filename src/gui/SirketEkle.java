    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         boolean durum = false;
        if(jTextField1.getText()==null || jTextField1.getText().length()==0){
            JOptionPane.showMessageDialog(this,"Bir müşteri seçiniz");
        }else{ 
        codes.sirketler sirket = new codes.sirketler();
        sirket.setH_kurum_no(jTextField1.getText());
        codes.bankalar1 bnk = new codes.bankalar1();
        int bnkno=0;
        try {
            bnkno = bnk.bankaNoBul(jComboBox1.getSelectedItem().toString().trim());
        } catch (SQLException ex) {
            Logger.getLogger(SirketEkle.class.getName()).log(Level.SEVERE, null, ex);
        }
        sirket.setH_banka_no(String.valueOf(bnkno));
        sirket.setH_hesap_adi(jTextField5.getText());
        sirket.setH_hesap_no(jTextField6.getText());
        sirket.setH_sube_no(jTextField7.getText());
        sirket.setH_iban_no("TR"+jTextField8.getText());
        try {
            durum = sirket.musteriHesabiEkle(sirket);
        }catch (SQLException ex) {
            //Logger.getLogger(SirketEkle.class.getName()).log(Level.SEVERE, null, ex);
            if(!durum)
                JOptionPane.showMessageDialog(this, "Hata: "+ex);
        }
        if(durum){
            JOptionPane.showMessageDialog(null, "Hesap Başarıyla Eklendi");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
            jTextField8.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "Hata");
        }
            
         
        }
    }//GEN-LAST:event_jButton4ActionPerformed

