    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int id;
        
      if (evt.getClickCount() ==2) {
      JTable target = (JTable)evt.getSource();
      int row = target.getSelectedRow();
      int column = target.getSelectedColumn();
        id =Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        List<codes.sirketler> sirketlist = null;
        try {
            sirketlist = new codes.sirketler().sirketAra(id);
        } catch (SQLException ex) {
            Logger.getLogger(sirketler.class.getName()).log(Level.SEVERE, null, ex);
        }
        int i=0;
        while(i<=sirketlist.size()){
            jTextField2.setText(sirketlist.get(i).getS_adi());
            jTextField1.setText(sirketlist.get(i).getS_sicilno());
            jTextArea1.setText(sirketlist.get(i).getS_adres());
            jTextField4.setText(sirketlist.get(i).getS_mail());
            jTextField3.setText(sirketlist.get(i).getS_yonetici());
            jFormattedTextField1.setText(sirketlist.get(i).getS_telefon());
            i++;
        }
        }
    }//GEN-LAST:event_jTable1MouseClicked

