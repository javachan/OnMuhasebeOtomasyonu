    public void faturaBul(boolean tumu,int fatura){
        List<codes.fatura> faturalist = null;//new ArrayList<>();
        DefaultTableModel faturatablo = (DefaultTableModel) jTable1.getModel();
        faturatablo.getDataVector().clear();
        faturatablo.setRowCount(0);
      
        try {
            faturalist= new codes.fatura().faturaDetayListele(false, fatura);
        } catch (SQLException ex) {
            Logger.getLogger(faturaGoruntule.class.getName()).log(Level.SEVERE, null, ex);
        }
        Vector[] v = new Vector[faturalist.size()];
        for(int n=0;n<faturalist.size();n++){
            String kurumAdi = "";
            try {
                 kurumAdi = new codes.sirketler().sirketAdiAl(faturalist.get(n).getF_kurum_no());
            } catch (SQLException ex) {
                Logger.getLogger(faturaGoruntule.class.getName()).log(Level.SEVERE, null, ex);
            }
            v[n] = new Vector();
            v[n].add(faturalist.get(n).getF_miktar());
            v[n].add(faturalist.get(n).getF_aciklama());
            v[n].add(faturalist.get(n).getF_tutar());
            jTextField10.setText(String.valueOf(faturalist.get(n).getF_kdv()));
            Kdv_Oran = faturalist.get(n).getF_kdv();
            this.Toplam+=(faturalist.get(n).getF_miktar() * faturalist.get(n).getF_tutar());
            faturatablo.insertRow(n, v[n]);
        }
        Kdv = Toplam * Kdv_Oran;
        Genel_Toplam = Toplam + Kdv;
        jTextField9.setText(String.valueOf(Toplam));
        jTextField11.setText(String.valueOf(Kdv));
        jTextField12.setText(String.valueOf(Genel_Toplam));
        jTextField10.setText(String.valueOf(Kdv_Oran*100));
        jTable1.setModel(faturatablo);
         jTable1.repaint();
    }
