    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        boolean asilDurum = true;boolean hata1=false;
        try{
            if(!Kurum_no.equals("") && !jTextField5.getText().equals("") && Toplam!=0 && Kdv_Oran!=0 && tablo.getRowCount()!=0 && asilDurum){
                codes.fatura fatura = new codes.fatura();
                String dtarih = jComboBox4.getSelectedItem().toString()+jComboBox3.getSelectedItem().toString()+jComboBox2.getSelectedItem().toString();
                String starih = jComboBox7.getSelectedItem().toString()+jComboBox6.getSelectedItem().toString()+jComboBox5.getSelectedItem().toString();
                String saat = jTextField1.getText().trim()+jTextField2.getText().trim()+"00";

                for(int i=0;i<tablo.getRowCount();i++){
                    boolean durum=false;
                    fatura.setF_fatura_no(Integer.parseInt(jTextField5.getText().trim()));
                    fatura.setF_kurum_no(Kurum_no);
                    fatura.setF_miktar(Integer.parseInt(tablo.getValueAt(i, 0).toString().trim()));
                    fatura.setF_aciklama(tablo.getValueAt(i, 1).toString());
                    fatura.setF_tutar(Float.parseFloat(tablo.getValueAt(i, 2).toString()));
                    fatura.setF_dtarih(dtarih);
                    fatura.setF_starih(starih);
                    fatura.setF_ssaat(saat);
                    fatura.setF_kdv(Kdv_Oran);
                    try {
                        durum = new codes.fatura().faturaKaydet(fatura);
                    } catch (SQLException ex) {
                        Logger.getLogger(FaturaEkle.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    if(!durum){
                        asilDurum=false;
                        break;}
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"Lütfen Tüm Alanları Doldurunuz");
                asilDurum=false;
                hata1=true;
            }
            if(asilDurum){
                JOptionPane.showMessageDialog(this, "Başarılı");
                Toplam = 0;
                Genel_Toplam = 0;
                Kdv=0;
                tablo.setRowCount(0);
                Kurum_no="";
                jComboBox1.setSelectedIndex(0);
                jTextArea1.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                faturaEkleAcilis();
            }
            else if(!asilDurum && !hata1){
                JOptionPane.showMessageDialog(this, "KAYDEDİLEMEDİ", "HATA", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(NumberFormatException | HeadlessException e){
            JOptionPane.showMessageDialog(this, "KAYDEDİLEMEDİ \n"+e, "HATA", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

