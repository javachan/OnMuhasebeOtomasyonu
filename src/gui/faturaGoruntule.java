    private void tumunuGoster(){
        List<codes.fatura> faturalist = null;//new ArrayList<>();
        DefaultTableModel faturatablo = (DefaultTableModel) jTable1.getModel();
        faturatablo.getDataVector().clear();
        faturatablo.setRowCount(0);
      
        try {
            faturalist= new codes.fatura().faturaListele(true, "");
        } catch (SQLException ex) {
            Logger.getLogger(faturaGoruntule.class.getName()).log(Level.SEVERE, null, ex);
        }
        Vector[] v = new Vector[faturalist.size()];
        for(int n=0;n<faturalist.size();n++){
            String kurumAdi = "";
            try {
                 kurumAdi = new codes.sirketler().sirketAdiAl(faturalist.get(n).getF_kurum_no());
            } catch (SQLException ex) {
                Logger.getLogger(faturaGoruntule.class.getName()).log(Level.SEVERE, null, ex);
            }
            v[n] = new Vector();
            v[n].add(faturalist.get(n).getF_fatura_no());
            float kdv = faturalist.get(n).getF_ToplamTutar() * faturalist.get(n).getF_kdv();
            kdv += faturalist.get(n).getF_ToplamTutar();
            v[n].add(kdv);
            v[n].add(kurumAdi);
            v[n].add(faturalist.get(n).getF_dtarih());
            faturatablo.insertRow(n, v[n]);
        }
        jTable1.setModel(faturatablo);
         jTable1.repaint();
    }
    private void faturaGosterKurumNo(String kurumno){
        List<codes.fatura> faturalist = null;//new ArrayList<>();
        DefaultTableModel faturatablo = (DefaultTableModel) jTable1.getModel();
        faturatablo.getDataVector().clear();
        faturatablo.setRowCount(0);
      
        try {
            faturalist= new codes.fatura().faturaListele(false, kurumno);
        } catch (SQLException ex) {
            Logger.getLogger(faturaGoruntule.class.getName()).log(Level.SEVERE, null, ex);
        }
        Vector[] v = new Vector[faturalist.size()];
        for(int n=0;n<faturalist.size();n++){
            String kurumAdi = "";
            try {
                 kurumAdi = new codes.sirketler().sirketAdiAl(faturalist.get(n).getF_kurum_no());
            } catch (SQLException ex) {
                Logger.getLogger(faturaGoruntule.class.getName()).log(Level.SEVERE, null, ex);
            }
            v[n] = new Vector();
            v[n].add(faturalist.get(n).getF_fatura_no());
            float kdv = faturalist.get(n).getF_ToplamTutar() * faturalist.get(n).getF_kdv();
            v[n].add(faturalist.get(n).getF_ToplamTutar() + kdv);
            
            v[n].add(kurumAdi);
            v[n].add(faturalist.get(n).getF_dtarih());
            faturatablo.insertRow(n, v[n]);
        }
        jTable1.setModel(faturatablo);
         jTable1.repaint();
    }
