    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int faturano = 0;
        if(evt.getClickCount()==2){
            JTable target = (JTable)evt.getSource();
            int row = target.getSelectedRow();
            gui.FaturaEkle fatura = new gui.FaturaEkle();
            try {
                this.Kurum_No = new codes.sirketler().sicilAl(jTable1.getValueAt(row, 2).toString());
            } catch (SQLException ex) {
                Logger.getLogger(faturaGoruntule.class.getName()).log(Level.SEVERE, null, ex);
            }
            fatura.Kurum_no=this.Kurum_No;
            fatura.musteriAdi = jTable1.getValueAt(row, 2).toString();
            faturano=Integer.parseInt(jTable1.getValueAt(row, 0).toString());
            
            JFrame jf = new JFrame();
            jf.add(fatura.getContentPane());
            jf.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
            jf.setVisible(true);
            fatura.faturaGoster(faturano);
            jf.setSize(new Dimension(970,1000));
            jf.setTitle("Fatura Detay");
            jf.setAlwaysOnTop(true);
            jf.setContentPane(fatura.getContentPane());
        }
    }//GEN-LAST:event_jTable1MouseClicked

